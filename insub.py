# try to find location of figlet and cowsay files

def find_share(name):
    """Find share directories of the provided name"""
    for prefix in ('/opt/local', '/usr/local', '/usr', '/'):
        for subdir in ('', 'share'):
            path = os.path.join(prefix, subdir, name)
            if os.path.isdir(path):
                return path

